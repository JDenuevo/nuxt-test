<script>
import { onMounted, ref } from 'vue';
import { initModals } from 'flowbite';

export default {
  props: {
    generateNewId: Function, // Receive the prop
  },
  setup(props, { emit }) {
    const name = ref('');
    const color = ref('');
    const category = ref('');
    const price = ref('');

    onMounted(() => {
      initModals();
    });

    const handleAdd = () => {
      const newItem = {
        id: props.generateNewId(), // Use the prop to generate a new ID
        name: name.value,
        color: color.value,
        category: category.value,
        price: price.value,
      };

      emit('addItem', newItem);
    };

    return {
      name,
      color,
      category,
      price,
      handleAdd,
    };
  },
};
</script>

